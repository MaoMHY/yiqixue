package yiqixue.yiqixue.houtai.htModel;

public class Star {
    int sid;
    int uid;
    int aid;

    public Star(int sid, int uid, int aid) {
        this.sid = sid;
        this.uid = uid;
        this.aid = aid;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getAid() {
        return aid;
    }

    public void setAid(int aid) {
        this.aid = aid;
    }
}
